// Adding two numbers
class Main
{
	public static void main(String[] args) {
	   // byte dataType
	   byte num1 = 3;
	   byte num2 = 4;
	   // short dataType
	   short num3 = 212;
	   short num4 = 300;
	   // int dataType
	   int num5 = 49999;
	   int num6 = 59999;
	   
	   System.out.println("Adding byte numbers: "+(num1+num2));
	   System.out.println("Adding short numbers: "+(num2+num3));
	   System.out.println("Adding int numbers: "+(num5+num6));
	}
}
